    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        
        cursoAlteracao.setTipo((Tipo) cbTipoCursoAlteracao.getSelectedItem());
        cursoAlteracao.setAtivo(jCheckBox2.isSelected());
        
        new Curso_e_EscolaDAO().atualizar(cursoAlteracao);
        
        AlterarCurso.dispose();
        
        JOptionPane.showMessageDialog(null, "Curso atualizado.");
        
        PainelGerenciarCurso.fillTCursos();
        
        DefaultTableModel dtmTabela = (DefaultTableModel) tCursos.getModel();
        dtmTabela.setNumRows(0);
    }//GEN-LAST:event_jButton15ActionPerformed

