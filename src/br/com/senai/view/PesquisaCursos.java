    private void cbCidadeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbCidadeItemStateChanged
        
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            if (!cbCidade.getSelectedItem().toString().equalsIgnoreCase("Selecione...")) {
                
                if (jCheckBox1.isSelected()) {
                    fillTable(new CursoDAO().pesquisa("ce.ativo=true and ce.pk.escola.cidade.id =" + ((Cidade) cbCidade.getSelectedItem()).getId() + "", Config.usuario));
                } else {
                    fillTable(new CursoDAO().pesquisa("ce.ativo=false and ce.pk.escola.cidade.id =" + ((Cidade) cbCidade.getSelectedItem()).getId() + "", Config.usuario));
                    
                }
                
            } else {
                DefaultTableModel dtmTabela = (DefaultTableModel) tCursos.getModel();
                dtmTabela.setNumRows(0);
            }
            
        }
    }//GEN-LAST:event_cbCidadeItemStateChanged

    private void cbEscolaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbEscolaItemStateChanged
        
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            
            if (!cbEscola.getSelectedItem().toString().equalsIgnoreCase("Selecione...")) {
                
                if (!jCheckBox1.isSelected()) {
                    fillTable(new CursoDAO().pesquisa("ce.ativo=false and ce.pk.escola.id =" + ((Escola) cbEscola.getSelectedItem()).getId() + "", Config.usuario));
                } else {
                    fillTable(new CursoDAO().pesquisa("ce.ativo=true and ce.pk.escola.id =" + ((Escola) cbEscola.getSelectedItem()).getId() + "", Config.usuario));
                }
                
            }
            
        } else {
            DefaultTableModel dtmTabela = (DefaultTableModel) tCursos.getModel();
            dtmTabela.setNumRows(0);
        }

    }//GEN-LAST:event_cbEscolaItemStateChanged

    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        
        if (tCursos.getSelectedRowCount() > 0) {
            
            cursoAlteracao = (Curso_e_Escola) tCursos.getValueAt(tCursos.getSelectedRow(), 0);
            
            jLabel3.setText(cursoAlteracao.getPk().getCurso().getNome());
            cbTipoCursoAlteracao.setSelectedItem(cursoAlteracao.getTipo());
            jLabel7.setText(cursoAlteracao.getPk().getEscola().getNome());
            jCheckBox2.setSelected(cursoAlteracao.isAtivo());
            
            AlterarCurso.pack();
            AlterarCurso.setTitle("Alterar curso - ");
            Config.configurarJanela(AlterarCurso);
            AlterarCurso.setLocationRelativeTo(null);
            AlterarCurso.setVisible(true);
            
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um curso.");
        }
    }//GEN-LAST:event_jButton11ActionPerformed

    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
        
        if (tCursos.getSelectedRowCount() > 0) {
            
            Curso_e_Escola ce = (Curso_e_Escola) tCursos.getValueAt(tCursos.getSelectedRow(), 0);
            
            if (new MovimentacaoDAO().listarMovimentacoesDoCurso(ce).isEmpty()) {
                
                if (JOptionPane.showConfirmDialog(null, "Deseja excluir esse curso?\nNome:" + ce.getPk().getCurso().getNome() + "\nEscola: " + ce.getPk().getEscola().getNome() + "\n*N√£o ser√° poss√≠vel reverter o processo", "Confirmar exclus√£o", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    
                    new Curso_e_EscolaDAO().deletar(ce);
                    
                    JOptionPane.showMessageDialog(null, "Curso deletado.");
                    
                    PainelGerenciarCurso.fillTCursos();
                    
                    DefaultTableModel dtmTabela = (DefaultTableModel) tCursos.getModel();
                    dtmTabela.setNumRows(0);
                    
                }
            } else {
                
                if (Config.mensagemDeConfirmacaoExclusao("Curso com movimenta√ß√£o.\n Deseja torn√°-lo inativo ?\nNome: " + ce.getPk().getCurso().getNome() + "\nEscola: " + ce.getPk().getEscola().getNome()) == JOptionPane.YES_OPTION) {
                    
                    ce.setAtivo(false);
                    
                    new Curso_e_EscolaDAO().atualizar(ce);
                    
                    JOptionPane.showMessageDialog(null, "Curso atualizado.");
                    
                    PainelGerenciarCurso.fillTCursos();
                    
                    DefaultTableModel dtmTabela = (DefaultTableModel) tCursos.getModel();
                    dtmTabela.setNumRows(0);
                    
                }
                
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um curso.");
        }

    }//GEN-LAST:event_jButton12ActionPerformed

