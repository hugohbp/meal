    private void fillTTurmas() {

        DefaultTableModel tableModel = (DefaultTableModel) tTurmas.getModel();

        tableModel.setNumRows(0);

        for (Turma turma : new TurmaDAO().turmasPermitidas(Config.usuario)) {
            tableModel.addRow(new Object[]{turma, turma.getNome(), turma.getCurso().getPk().getCurso(), turma.getTurno(), turma.getCurso().getPk().getEscola()});
        }

    }
