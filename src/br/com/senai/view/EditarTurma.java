    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed

        if (!tfNomeTurma.getText().isEmpty() && !cbCurso.getSelectedItem().toString().equalsIgnoreCase("Selecione...") && !jComboBox1.getSelectedItem().toString().equalsIgnoreCase("Selecione...") && !cbTurno.getSelectedItem().toString().equalsIgnoreCase("Selecione...")) {

            t.setNome(tfNomeTurma.getText());

            t.setTurno((Turno) cbTurno.getSelectedItem());

            t.setCurso(new Curso_e_EscolaDAO().getCurso_e_Escola((Curso) cbCurso.getSelectedItem(), (Escola) jComboBox1.getSelectedItem()));

            t.setAtiva(cbAtiva.isSelected());

            new TurmaDAO().atualizar(t);

            JOptionPane.showMessageDialog(null, "salvo.");

            tfNomeTurma.setText(null);

            try {
                PesquisaTurmas.fillTable("t.nome like '%%'");
            } catch (NullPointerException ex) {

            }

            dispose();

        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campos corretamente.", "Resposta", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

