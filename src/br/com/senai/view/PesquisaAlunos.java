    private void jButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton19ActionPerformed
        
        if (!tfNomeEditarAluno.getText().isEmpty()) {
            Aluno a = new Aluno();
            
            Turma t = new TurmaDAO().getNome(jLabel2.getText());
            a.setNome(tfNomeEditarAluno.getText());
            a.setAluno(Long.parseLong(jLabel1.getText()));
            a.setAtivo(cbAlunoAtivo.isSelected());
            a.setTurma(t);
            
            if (a.isAtivo()) {
                
                Aluno a1 = new AlunoDAO().validarTurma(a.getAluno(), t.getTurno());
                
                if (a1 != null) {
                    
                    if (JOptionPane.showConfirmDialog(null, "Foi encontrado uma turma:" + a1.getTurma().getNome() + " \n ativa no turno:" + a1.getTurma().getTurno().getTurno() + "\n com o usuário:" + a1.getAluno(), "Confirmar alteração de turma", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                        
                        a1.setAtivo(false);
                        new AlunoDAO().atualizar(a1);
                        
                        new AlunoDAO().atualizar(a);
                        
                        JOptionPane.showMessageDialog(null, "salvo com sucesso!");
                    }
                    
                } else {
                    new AlunoDAO().atualizar(a);
                    
                    JOptionPane.showMessageDialog(null, "salvo com sucesso!");
                }
                
            } else {
                new AlunoDAO().atualizar(a);
                
                JOptionPane.showMessageDialog(null, "salvo com sucesso!");
            }
            
            fillTable("a.pk.id like '%" + tfRaALuno.getText() + "%'");
            
            PainelAluno.atualizarAlunos();
            
            AlterarAluno.dispose();
            
            toFront();
        } else {
            Config.mensagemDeAviso("Preencha os campos corretamente");
        }
    }//GEN-LAST:event_jButton19ActionPerformed

