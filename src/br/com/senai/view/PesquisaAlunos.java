    public void fillTable(String query) {
        
        DefaultTableModel tableModel = (DefaultTableModel) tAlunosCadastrados.getModel();
        
        tableModel.setNumRows(0);
        
        for (Aluno aluno : new AlunoDAO().pesquisar(query, Config.usuario)) {
            
            tableModel.addRow(new Object[]{aluno, aluno.getAluno(), aluno.getTurma().getNome(), aluno.isAtivo()});
            
        }
        
    }

