    private void jButton29ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton29ActionPerformed
        if (tAlunosCadastrados.getSelectedRowCount() > 0) {
            
            try {
                
                Aluno a = (Aluno) tAlunosCadastrados.getValueAt(tAlunosCadastrados.getSelectedRow(), 0);
                String movimentacao = "";
                
                if (!new AlunoDAO().getMovimentacoes(a).isEmpty()) {
                    movimentacao = "Há movimentações para esse aluno.";
                }
                
                if (JOptionPane.showConfirmDialog(null, "Deseja excluir esse aluno?\nRA:" + a.getAluno() + "\nTurma:" + a.getTurma() + "\n*Não será possível reverter o processo\n" + movimentacao, "Confirmar exclusão", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    
                    if (new AlunoDAO().getMovimentacoes(a).isEmpty()) {
                        new AlunoDAO().deletar(a);
                        fillTable("a.pk.id like '%" + tfRaALuno.getText() + "%'");
                    } else {
                        a.setAtivo(false);
                        new AlunoDAO().atualizar(a);
                    }
                    
                    PainelAluno.atualizarAlunos();
                    
                    toFront();
                    
                }
            } catch (NullPointerException ex) {
                
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um aluno.");
        }
    }//GEN-LAST:event_jButton29ActionPerformed

