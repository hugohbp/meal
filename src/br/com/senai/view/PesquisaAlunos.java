    private void cbEscolaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbEscolaItemStateChanged
        
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            if (!cbEscola.getSelectedItem().toString().equalsIgnoreCase("Selecione...")) {
                
                Config.fillComboBox(cbCurso, new Curso_e_EscolaDAO().cursosDaEscola((Escola) cbEscola.getSelectedItem()));
                cbCurso.setEnabled(true);
            } else {
                
                cbCurso.removeAllItems();
                cbCurso.addItem(new Object() {
                    
                    @Override
                    public String toString() {
                        return "Selecione...";
                    }
                });
                cbCurso.setEnabled(false);
            }
        }
    }//GEN-LAST:event_cbEscolaItemStateChanged

    private void cbCursoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbCursoItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            
            if (!cbCurso.getSelectedItem().toString().equalsIgnoreCase("Selecione...")) {
                
                Config.fillComboBox(cbTurno, new TurnoDAO().listar());
                curso_e_Escola = new Curso_e_EscolaDAO().getCurso_e_Escola((Curso) cbCurso.getSelectedItem(), (Escola) cbEscola.getSelectedItem());
                Config.fillComboBox(cbTurma, new TurmaDAO().pesquisarPorCurso(curso_e_Escola));
            } else {
                Config.limparComboBox(cbTurma);
                
            }
        }
    }//GEN-LAST:event_cbCursoItemStateChanged

    private void cbTurnoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbTurnoItemStateChanged
        
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            
            if (cbTurno.getSelectedItem().toString().equalsIgnoreCase("Selecione...")) {
                turnoAtivado = false;
                Config.limparComboBox(cbTurma);
                
            } else {
                turnoAtivado = true;
                
                if (!cbCurso.getSelectedItem().toString().equalsIgnoreCase("Selecione...")) {
                    
                    Config.fillComboBox(cbTurma, new TurmaDAO().pesquisarPorCursoETurno(curso_e_Escola, (Turno) cbTurno.getSelectedItem()));
                    
                }
                
            }
        }
    }//GEN-LAST:event_cbTurnoItemStateChanged

    private void cbTurmaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbTurmaItemStateChanged
        
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            
            if (!cbTurma.getSelectedItem().toString().equalsIgnoreCase("Selecione...")) {
                
                if (jCheckBox1.isSelected()) {
                    fillTable("a.pk.turma.nome='" + cbTurma.getSelectedItem().toString() + "' and a.ativo=true");
                } else {
                    fillTable("a.pk.turma.nome='" + cbTurma.getSelectedItem().toString() + "' and a.ativo=false");
                }
                
                cbTurma.setSelectedIndex(0);
                
            }
        }
    }//GEN-LAST:event_cbTurmaItemStateChanged

