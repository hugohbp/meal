    public static void fillTCursos() {

        DefaultTableModel tableModel = (DefaultTableModel) tCursos.getModel();

        tableModel.setNumRows(0);

        for (Curso_e_Escola curso : new CursoDAO().cursosPermitidos(Config.usuario)) {
            tableModel.addRow(new Object[]{curso, curso.getPk().getCurso().getNome(), curso.getTipo().getNome(), curso.getPk().getEscola()});

        }
    }

