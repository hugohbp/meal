    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        if (!cbCursos.getSelectedItem().toString().equalsIgnoreCase("Selecione...") && !cbTipo.getSelectedItem().toString().equalsIgnoreCase("Selecione...") && !cbEscolaCurso.getSelectedItem().toString().equalsIgnoreCase("Selecione...")) {

            try {
                Curso_e_Escola c = new Curso_e_Escola();

                c.getPk().setCurso((Curso) cbCursos.getSelectedItem());
                c.getPk().setEscola((Escola) cbEscolaCurso.getSelectedItem());
                c.setTipo((Tipo) cbTipo.getSelectedItem());
                c.setAtivo(true);

                new Curso_e_EscolaDAO().salvar(c);

                JOptionPane.showMessageDialog(null, "Cadastrado com sucesso.");

                fillTCursos();

            } catch (ConstraintViolationException ex) {
                JOptionPane.showMessageDialog(null, "Curso jÃ¡ cadastrado na escola.");

            }
        } else {
            JOptionPane.showMessageDialog(null, "Preecha os campos.");
        }
    }//GEN-LAST:event_jButton22ActionPerformed

