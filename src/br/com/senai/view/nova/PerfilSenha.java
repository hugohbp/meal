    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if (!pfSenhaAtual.getText().isEmpty() && !pfNovaSenha.getText().isEmpty() && !pfConfirmarSenha.getText().isEmpty()) {
            
            if (Config.criptografarSenha(pfSenhaAtual.getPassword()).equals(Config.usuario.getSenha())) {
                
                if (Arrays.equals(pfNovaSenha.getPassword(), pfConfirmarSenha.getPassword())) {
                    
                    Config.usuario.setSenha(Config.criptografarSenha(pfConfirmarSenha.getPassword()));
                    
                    Config.usuario.setSenhaAlterada(false);
                    
                    JOptionPane.showMessageDialog(null, "Informações salvas com sucesso", "Resposta", JOptionPane.DEFAULT_OPTION);
                    
                    new UsuarioDAO().atualizar(Config.usuario);
                    
                    System.gc();
                    
                    for (Window w : Window.getWindows()) {
                        w.dispose();
                    }
                    
                    new Login().setVisible(true);
                    
                } else {
                    JOptionPane.showMessageDialog(null, "Senhas não correspondem...");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Senha atual errada.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Campos vazios");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


